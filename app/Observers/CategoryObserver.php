    public function updated(Category $category)
    {
        $changes = $category->getChanges();

        if (isset($changes['is_active'])) {
            $active = $changes['is_active'];


            try {
                DB::beginTransaction();

                $category->chields()->update(['is_active' => $active]);


                DB::commit();
            } catch (\Throwable $th) {
                DB::rollback();
            }
        }
    }

